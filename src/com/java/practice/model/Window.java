package com.java.practice.model;

public class Window {
	
	private int numOfWindow;
	private int numOfDoorInWindow;
	private String metalOfWindow;
	private int lengthOfWindow;
	private int numOfLayers;
	private String colourOfWindow;
	public int getNumOfWindow() {
		return numOfWindow;
	}
	public void setNumOfWindow(int numOfWindow) {
		this.numOfWindow = numOfWindow;
	}
	public int getNumOfDoorInWindow() {
		return numOfDoorInWindow;
	}
	public void setNumOfDoorInWindow(int numOfDoorInWindow) {
		this.numOfDoorInWindow = numOfDoorInWindow;
	}
	public String getMetalOfWindow() {
		return metalOfWindow;
	}
	public void setMetalOfWindow(String metalOfWindow) {
		this.metalOfWindow = metalOfWindow;
	}
	public int getLengthOfWindow() {
		return lengthOfWindow;
	}
	public void setLengthOfWindow(int lengthOfWindow) {
		this.lengthOfWindow = lengthOfWindow;
	}
	public int getNumOfLayers() {
		return numOfLayers;
	}
	public void setNumOfLayers(int numOfLayers) {
		this.numOfLayers = numOfLayers;
	}
	public String getColourOfWindow() {
		return colourOfWindow;
	}
	public void setColourOfWindow(String colourOfWindow) {
		this.colourOfWindow = colourOfWindow;
	}
	
}
